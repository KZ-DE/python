# len(digits) - 1: Menentukan nilai awal loop, yaitu indeks terakhir dari list digits. Fungsi len(digits) mengembalikan panjang (jumlah elemen) dari list digits, dan dikurangkan 1 untuk mendapatkan indeks terakhir.

# -1: Menentukan nilai akhir loop, yaitu -1. Ini menandakan bahwa loop akan berjalan hingga mencapai indeks -1 (indeks sebelum elemen pertama).

# -1: Menentukan langkah (step) loop, yaitu -1. Ini menunjukkan bahwa loop akan mengurangi nilai indeks pada setiap iterasi.
