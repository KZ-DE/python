# operator assignment
# operasi yang dpat dilakukan dengan penyingkatan
# operasi di tambah dengan assignment


a = 5 #adalah assignment
print('nilai a = ',a)


# operasi tambah(+=)
a = a + 1
print('nilai a = ', a)
# atau bsia di singkat
a += 1 # artinya adalah a = a + 1
print('nilai a menjadi = ', a)

# operasi pengurangan(-=)
a -= 2
print('nilai a menjadi = ',a)

# operasi perkalian (*=)
a *= 6
print('nilai a menjadi = ',a)

# operasi perkalian (/=)
a /= 6
print('nilai a menjadi = ',a)

b = 10
print('\nnilai b = ',b)

b %= 3 # berapa sisa pembagian 10 dibagi 3
print('nilai b %= ',b)

b = 10
print('\nnilai b = ',b)

b //= 3 # berapa sisa pembagian 10 dibagi 3
print('nilai b //= ',b)

a = 5
print('nilai a = ',a)
a **= 3
print('nilai a **= ',a)


# operasi bitwise
c = False
print('\nnilai c = ',c)
c |= False
print('nilai c menjadi = ', c)