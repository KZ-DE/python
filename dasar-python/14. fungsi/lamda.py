# Fungsi lambda dalam Python adalah cara untuk membuat fungsi anonim atau fungsi tanpa nama

# format penulisan

# lambda arguments: expression
# arguments: Parameter atau argumen yang diperlukan oleh fungsi.
# expression: Ekspresi atau pernyataan yang dievaluasi oleh fungsi.

def square(x): return x**2


print(square(5))  # Output: 25
